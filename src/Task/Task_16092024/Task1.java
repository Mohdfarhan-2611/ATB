package Task.Task_16092024;

public class Task1 {

    public static void main(String[] args) {

        //Difference between = &  ==
        int a = 10;  // with single = we can assign right side value to the variable
        int b = 20;  // with single = we can assign right side value to the variable
        int c = 10;  // with single = we can assign right side value to the variable
                     // it requires single operands hence unary operator

        System.out.println(a==b); // with double == we can compare two operands
        System.out.println(a==c); // with double == we can compare two operands
                     // it requires two operands hence binary operator
    }
}
