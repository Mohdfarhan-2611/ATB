public class Java007 {
    public static void main(String[] args) {
        //Write a program to print all odd number between 1 to 100

        for(int i=0; i<=100; i++)
        {
            if(i%2!=0)
            {
                System.out.println("Odd " + i);
            }
        }
    }
}

