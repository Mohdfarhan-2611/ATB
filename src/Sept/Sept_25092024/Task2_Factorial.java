package Sept.Sept_25092024;

public class Task2_Factorial {
    public static void main(String[] args) {

        int num = 10;
        int fact = 1;

        for(int i =1; i<= num; i++)
        {
            i = i * 1;
            fact = fact * i;
        }

        System.out.println(fact);


    }
}
